// Array.map(): This method creates a new array by applying a function to each element of the original array. It's useful for transforming data in arrays without mutating the original array.

// Array.filter(): This method creates a new array with all elements that pass the test implemented by the provided function. It's great for filtering out unwanted elements from an array.

// Array.reduce(): This method applies a function against an accumulator and each element in the array (from left to right) to reduce it to a single value. It's powerful for performing calculations on array elements.

// Array.forEach(): This method executes a provided function once for each array element. It's handy for iterating over array elements without the need to create a new array.

// Array.includes(): This method determines whether an array includes a certain value among its entries, returning true or false as appropriate. It's useful for checking if an array contains a specific element.

// Array.find(): This method returns the value of the first element in the provided array that satisfies the provided testing function. It's helpful for searching for an element in an array based on a condition.

// Array.some(): This method tests whether at least one element in the array passes the test implemented by the provided function. It's useful for checking if any element satisfies a given condition.

// Array.every(): This method tests whether all elements in the array pass the test implemented by the provided function. It's handy for checking if all elements satisfy a given condition.

// Array.sort(): This method sorts the elements of an array in place and returns the sorted array. It's essential for sorting arrays based on various criteria.

// Array.slice(): This method returns a shallow copy of a portion of an array into a new array object selected from start to end (end not included) where start and end represent the index of items in that array. It's useful for extracting a portion of an array without modifying the original array.



